    private void btnsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsaveActionPerformed
        // TODO add your handling code here:
    String Tampilan="yyyy-MM-dd";
    SimpleDateFormat fm=new SimpleDateFormat(Tampilan);
    String tglbyr=String.valueOf(fm.format(tgl.getDate()));
    try {
    PreparedStatement ps = con.prepareStatement("INSERT INTO pembayaran VALUES (?,?,?,?,?,?,?,?)");
    ps.setString(1, txtid.getText());
    ps.setString(2, cbidpetugas.getSelectedItem().toString());
    ps.setString(3, cbnisn.getSelectedItem().toString());
    ps.setString(4, tglbyr);
    ps.setString(5, cbbulan.getSelectedItem().toString());
    ps.setInt(6, tahun.getYear());
    ps.setString(7, cbidspp.getSelectedItem().toString());
    ps.setString(8, txtjml.getText());
    ps.executeUpdate();
    JOptionPane.showMessageDialog(this,"Data telah tersimpan");
    refreshTable();           
    } catch(Exception e) {
    System.out.print("ERROR KUERI KE DATABASE:\n" + e + "\n\n");
    }
    }//GEN-LAST:event_btnsaveActionPerformed

