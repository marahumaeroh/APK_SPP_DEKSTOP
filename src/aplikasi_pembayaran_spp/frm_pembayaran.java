private void refreshTable(){
 tm = new DefaultTableModel(null,
    new Object[] {"Id Pembayaran","Id Petugas","NISN","tgl_bayar", "bulan_bayar", "tahun_bayar", "Id SPP", "Jumlah Bayar"});
    tblbayar.setModel(tm);
    tm.getDataVector().removeAllElements();
    try {
        PreparedStatement s = con.prepareStatement("SELECT*FROM pembayaran");
        ResultSet r = s.executeQuery();
        while(r.next()){
        Object[] data ={ 
        r.getString(1),
        r.getString(2),
        r.getString(3),
        r.getString(4),
        r.getString(5),
        r.getString(6),
        r.getString(7),
        r.getString(8)
                };
        tm.addRow(data);
        }
 } catch(Exception e) {
 System.out.print("ERROR KUERI KE DATABASE:\n" + e + "\n\n");
 }
}

public void cbidpetugas(){
try {
PreparedStatement s = con.prepareStatement("SELECT*FROM petugas where id_petugas=2");
ResultSet r = s.executeQuery();
    while (r.next()) {                
    cbidpetugas.addItem(r.getString("id_petugas"));
    }
} catch (SQLException e) {
}
 }
public void cbnisn(){
try {
PreparedStatement s = con.prepareStatement("SELECT*FROM siswa");
ResultSet r = s.executeQuery();
    while (r.next()) {                
    cbnisn.addItem(r.getString("NISN"));
    }
} catch (SQLException e) {
}
 }
public void cbidspp(){
try {
PreparedStatement s = con.prepareStatement("SELECT*FROM spp");
ResultSet r = s.executeQuery();
    while (r.next()) {                
    cbidspp.addItem(r.getString("id_spp"));
    }
} catch (SQLException e) {
}
 }

