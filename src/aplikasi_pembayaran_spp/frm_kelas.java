    private void tblkelasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblkelasMouseClicked
        // TODO add your handling code here:
     int i = tblkelas.getSelectedRow();
     if (i == -1){
          //tidak ada baris terseleksi
         return;
     }
     
     String idkls = (String) tm.getValueAt(i, 0);
     txtid.setText(idkls);
     
     String namakls = (String) tm.getValueAt(i, 1);
     txtnama.setText(namakls);
     
     String kom = (String) tm.getValueAt(i, 2);
     cbkom.setSelectedItem(kom);
     
    }//GEN-LAST:event_tblkelasMouseClicked

