    private void tblsiswaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblsiswaMouseClicked
        // TODO add your handling code here:
        int i = tblsiswa.getSelectedRow();
     if (i == -1){
          //tidak ada baris terseleksi
         return;
     }
     
     String nisn = (String) tm.getValueAt(i, 0);
     txtnisn.setText(nisn);
     
     String nis = (String) tm.getValueAt(i, 1);
     txtnis.setText(nis);
     
     String nama = (String) tm.getValueAt(i, 2);
     txtnama.setText(nama);
     
     String idkls = (String) tm.getValueAt(i, 3);
     cbidkelas.setSelectedItem(idkls);
     
     String alamat = (String) tm.getValueAt(i, 4);
     txtalamat.setText(alamat);
     
     String no = (String) tm.getValueAt(i, 5);
     txtno.setText(no);
     
     String idspp = (String) tm.getValueAt(i, 6);
     cbidspp.setSelectedItem(idspp);
    }//GEN-LAST:event_tblsiswaMouseClicked

