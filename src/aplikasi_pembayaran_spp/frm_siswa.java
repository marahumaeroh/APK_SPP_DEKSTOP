 private void refreshTable(){
 tm = new DefaultTableModel(null,
    new Object[] {"NISN","NIS","Nama","Id Kelas", "Alamat", " No Telp", "Id SPP"});
    tblsiswa.setModel(tm);
    tm.getDataVector().removeAllElements();
    try {
        PreparedStatement s = con.prepareStatement("SELECT*FROM siswa");
        ResultSet r = s.executeQuery();
        while(r.next()){
        Object[] data ={ 
        r.getString(1),
        r.getString(2),
        r.getString(3),
        r.getString(4),
        r.getString(5),
        r.getString(6),
        r.getString(7)
                };
        tm.addRow(data);
        }
 } catch(Exception e) {
 System.out.print("ERROR KUERI KE DATABASE:\n" + e + "\n\n");
 }
}
 
 public void cbidkelas(){
    try {
    PreparedStatement s = con.prepareStatement("SELECT*FROM kelas");
    ResultSet r = s.executeQuery();
    while (r.next()) {                
    cbidkelas.addItem(r.getString("id_kelas"));
    }
    } catch (SQLException e) {
    }
 }
 public void cbidspp(){
    try {
    PreparedStatement s = con.prepareStatement("SELECT*FROM spp");
    ResultSet r = s.executeQuery();
    while (r.next()) {                
    cbidspp.addItem(r.getString("id_spp"));
    }
    } catch (SQLException e) {
    }
 }
 
 
